
public class Streamers {

    String streamerType;
    String id;
    String name;

    public Streamers(String streamerType, String id, String name) {
        this.streamerType = streamerType;
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }


}
